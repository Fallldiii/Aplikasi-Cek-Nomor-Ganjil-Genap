    private void btnCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekActionPerformed
String input = txtAngka.getText().trim();
    if (input.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Input tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int angka = Integer.parseInt(input);

        // Cek genap/ganjil
        String hasil = (angka % 2 == 0) ? "Angka " + angka + " adalah GENAP" 
                                        : "Angka " + angka + " adalah GANJIL";

        // Cek bilangan prima (variasi)
        if (isPrima(angka)) {
            hasil += "<br> dan merupakan BILANGAN PRIMA";
        }

        JOptionPane.showMessageDialog(this, hasil, "Hasil", JOptionPane.INFORMATION_MESSAGE);
        labelHasil.setText("<html>" + hasil + "</html>");
        labelHasil.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);


    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Input harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnCekActionPerformed

